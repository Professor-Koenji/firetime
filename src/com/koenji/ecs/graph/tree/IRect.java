package com.koenji.ecs.graph.tree;

public interface IRect {
  /**
   * @return Get the x-value of this Rectangle.
   */
  float getX();

  /**
   * @return Get the y-value of this Rectangle.
   */
  float getY();

  /**
   * @return Get the width of this Rectangle.
   */
  float getW();

  /**
   * @return Get the height of this Rectangle.
   */
  float getH();
}
